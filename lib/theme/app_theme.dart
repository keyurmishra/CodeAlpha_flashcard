  // Adaptive Learning Palette - Color Specifications
  static const Color primaryLight =
      Color(0xFF2563EB); // Deep blue for primary actions
  static const Color primaryDark =
      Color(0xFF3B82F6); // Lighter blue for dark theme
  static const Color secondaryLight =
      Color(0xFF64748B); // Neutral slate for supporting elements
  static const Color secondaryDark =
      Color(0xFF94A3B8); // Lighter slate for dark theme
  static const Color accentLight =
      Color(0xFF10B981); // Success green for positive feedback
  static const Color accentDark =
      Color(0xFF34D399); // Lighter green for dark theme

  // Background colors optimized for extended study sessions
  static const Color backgroundLight = Color(0xFFFAFAFA); // Warm off-white
  static const Color backgroundDark =
      Color(0xFF0F172A); // Deep slate for dark theme
  static const Color surfaceLight = Color(0xFFFFFFFF); // Pure white for cards
  static const Color surfaceDark = Color(0xFF1E293B); // Dark slate for cards

  // Text colors with high contrast ratios
  static const Color textPrimaryLight =
      Color(0xFF1E293B); // Dark slate for primary text
  static const Color textPrimaryDark =
      Color(0xFFF8FAFC); // Light slate for dark theme
  static const Color textSecondaryLight =
      Color(0xFF64748B); // Medium gray for supporting text
  static const Color textSecondaryDark =
      Color(0xFF94A3B8); // Lighter gray for dark theme

  // Border and divider colors
  static const Color borderSubtleLight =
      Color(0xFFE2E8F0); // Light gray for minimal borders
  static const Color borderSubtleDark =
      Color(0xFF334155); // Dark gray for dark theme borders

  // Status colors
  static const Color errorLight =
      Color(0xFFDC2626); // Clear red for error states
  static const Color errorDark =
      Color(0xFFEF4444); // Lighter red for dark theme
  static const Color warningLight =
      Color(0xFFF59E0B); // Amber for caution states
  static const Color warningDark =
      Color(0xFFFBBF24); // Lighter amber for dark theme

  // Shadow colors for contextual card elevation
  static const Color shadowLight =
      Color(0x0F000000); // Subtle shadow for light theme
  static const Color shadowDark =
      Color(0x1F000000); // Slightly stronger shadow for dark theme

  /// Light theme optimized for educational mobile applications
  static ThemeData lightTheme = ThemeData(
